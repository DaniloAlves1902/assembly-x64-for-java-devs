public class IfElse {
    public static void main(String[] args) {
        int numero = -5;

        if (numero > 0) {
            System.out.println("O numero e positivo, number is positive");
        } else if (numero < 0) {
            System.out.println("O numero e negativo, number is negative");
        } else {
            System.out.println("O numero e zero, number is zero");
        }
    }
}
