public class IfElse {
    public static void main(String[] args) {
        int numero = -5;

        if (numero > 0) {
            System.out.println("O numero e positivo");
        } else if (numero < 0) {
            System.out.println("O numero e negativo");
        } else {
            System.out.println("O numero e zero");
        }
    }
}
